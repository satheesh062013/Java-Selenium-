package com.Nested;

public class Nested_If_Else_Demo {

	public static void main(String[] args) 
	{
		String likes = "Walking";
		
		if(likes=="Food")
			System.out.println("I Likes to eat "+likes);
		else if(likes=="Cinema")
			System.out.println("I Likes to Watch "+likes);
		else if(likes=="Walking")
			System.out.println("I Likes to go for "+likes);
		else
			System.out.println("I Likes to do Nothing");
	}

}
